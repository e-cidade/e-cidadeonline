   function sql_query ( $q92_codigo=null,$campos="*",$ordem=null,$dbwhere=""){ 
     $sql = "select ";
     if($campos != "*" ){
       $campos_sql = split("#",$campos);
       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }else{
       $sql .= $campos;
     }
     $sql .= " from cadvencdesc ";
     $sql .= "      inner join arretipo  on  arretipo.k00_tipo = cadvencdesc.q92_tipo";
     $sql .= "      inner join bancos  on  bancos.codbco = cadvencdesc.q92_codbco";
     $sql .= "      inner join cadban  on  cadban.k15_codbco = cadvencdesc.q92_codbco and  cadban.k15_codage = cadvencdesc.q92_codage";
     $sql .= "      inner join cgm  on  cgm.z01_numcgm = cadban.k15_numcgm";
     $sql .= "      inner join bancos  as a on   a.codbco = cadban.k15_codbco";
     $sql2 = "";
     if($dbwhere==""){
       if($q92_codigo!=null ){
         $sql2 .= " where cadvencdesc.q92_codigo = $q92_codigo "; 
       } 
     }else if($dbwhere != ""){
       $sql2 = " where $dbwhere";
     }
     $sql .= $sql2;
     if($ordem != null ){
       $sql .= " order by ";
       $campos_sql = split("#",$ordem);
       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }
     return $sql;
  }
