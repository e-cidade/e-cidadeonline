   // funcao para atualizar campos
   function atualizacampos($exclusao=false) {
     if($exclusao==false){
       $this->inicial = ($this->inicial == ""?@$GLOBALS["HTTP_POST_VARS"]["inicial"]:$this->inicial);
       $this->advog = ($this->advog == ""?@$GLOBALS["HTTP_POST_VARS"]["advog"]:$this->advog);
